export const getProducts = (page = 1, limit = 8, filter?: string) => {
  const start = (page - 1) * limit;
  const end = start + limit;
  let filtered = [...mockProducts];

  if (filter === "nearby") {
    filtered = filtered.filter(
      (p) =>
        p.location.lat > 40.7 &&
        p.location.lat < 40.73 &&
        p.location.lng > -74.01 &&
        p.location.lng < -73.99
    );
  } else if (filter === "category") {
    filtered = filtered.filter((p) => p.categoryId !== null);
  } else if (filter === "city") {
    filtered = filtered.filter((p) => p.address.includes("New York"));
  }

  filtered.sort((a, b) => b.createdAt.getTime() - a.createdAt.getTime());

  return {
    products: filtered.slice(start, end),
    total: filtered.length,
    totalPages: Math.ceil(filtered.length / limit),
  };
};

